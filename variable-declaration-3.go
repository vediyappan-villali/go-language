// Go program to assign values to variables after declaration

package main

import "fmt"

func main() {
    var name string
    var age int
    var salary float32
    var flag bool

    name = "Vediyappan"
    age = 27
    salary = 1000.55
    flag = true

    fmt.Println("name = ", name)
    fmt.Println("age = ", age)
    fmt.Println("salary = ", salary)
    fmt.Println("flag = ", flag)
}
